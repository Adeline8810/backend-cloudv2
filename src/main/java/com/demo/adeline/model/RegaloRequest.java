package com.demo.adeline.model;

public class RegaloRequest {
    private Long regaladorId;
    private Long destinatarioId;
    private Long idRegalo;

    public Long getRegaladorId() { return regaladorId; }
    public void setRegaladorId(Long regaladorId) { this.regaladorId = regaladorId; }
    public Long getDestinatarioId() { return destinatarioId; }
    public void setDestinatarioId(Long destinatarioId) { this.destinatarioId = destinatarioId; }
    public Long getIdRegalo() { return idRegalo; }
    public void setIdRegalo(Long idRegalo) { this.idRegalo = idRegalo; }
}